    /**
     * Método que determina movimiento y lo regresa como número
     *
     * @return
     */
    public int determinarMovimiento() {
        int movimiento = 0;
        for (int i = 0; i < partida.getCañas().length; i++) {
            if (partida.getCañas()[i].getLado()) {
                movimiento++;
            }
        }
        if (movimiento == 5) {
            movimiento = 10;
        }
        return movimiento;

    }

