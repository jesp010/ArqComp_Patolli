    /**
     * Este metodo verifica que la ficha este dentro de la casilla
     * @param Id
     * @param jugador
     * @return boolean
     */
    public boolean VerificarDentro(int Id, Jugador jugador) {
        //jugador dado
        Jugador j = jugador;
        //por las fichas dentro de la lista de fichas en jugador

        for (int i = 0; i < j.getFichas().length; i++) {
            if (j.getFichas()[i].getId() == Id) {
                Ficha f = j.getFichas()[i];
                if (f.getPosicion() == -1) {
                    //posicion diferene a 0
                    return true;
                } else {
                    return false;
                }
            }
        }
        //si el id dado es igual al id de la ficha y
        // y si la posicion es diferente de 0 entonces se manda un true    
        //no esta dentro
        return false;
    }
    
